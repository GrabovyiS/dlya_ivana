package interfaces

/**
 * Интерфейс классов обучения системы
 */
interface Teacher {
    /**
     * сеттер пороргового значения, пересечение которого считается успехом в распознавании
     * @param value пороговое значение
     */
    fun setThresholdValue(value: Double)
    /**
     * метод обучения, возвращает успех, или неудачу
     * @param input строка, на базе которой происходит обучение. Поступает сюда, если распознать сразу не удалось
     * @return возвращает результат обучения (успешно/не успешно)
     */
    fun study(input: String): Boolean
}
